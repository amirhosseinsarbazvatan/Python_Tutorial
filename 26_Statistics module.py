# Statistics module in python

# Work with statistics function in python


num =[3,5,7,11,9,0,1,6]

import statistics

# Mean
print(statistics.mean(num))

# Mode
print(statistics.mode(num))

# Median
print(statistics.median(num))

# Statndard deviation
print(statistics.stdev(num))

# Variance
print(statistics.variance(num))
